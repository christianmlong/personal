def flatten(theList):
    """
    Takes a nested list of lists, and returns one flat list.
    """
    out = []
    for theItem in theList:
        if isinstance(theItem, (list, tuple)):
            out.extend(flatten(theItem))
        else:
            out.append(theItem)
    return out

