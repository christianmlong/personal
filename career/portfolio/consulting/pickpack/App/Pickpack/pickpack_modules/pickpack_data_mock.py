def makeMeADeferred(desiredResult):
    """
    Sets up a deferred object, to mimic the deferred that is returned from a
    real database query.
    """
    if pickpack_constants.ENABLE_DELAY:
        deferred = defer.Deferred()
        # simulate a delayed result by asking the reactor to fire the Deferred
        # in DELAY_SECONDS time with the desiredResult
        reactor.callLater(pickpack_constants.DELAY_SECONDS, deferred.callback, desiredResult)      # pylint: disable=no-member
        return deferred
    else:
        # Return a Deferred object, already called back, with the value of
        # desiredResult.
        return defer.succeed(desiredResult)

