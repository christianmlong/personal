def isValidSerialNumberData_deferred(item_number,
                                     serial_number,
                                    ):
    """
    Returns one row with the requested item number, if the number appears in
    MOCK_SERIAL_NUMBERS; used for testing.
    """
    builder = pickpack_result_builders.IsValidSerialNumberBuilder()

    concatenatedKey = (item_number, serial_number)
    if concatenatedKey in USED_SERIAL_NUMBERS:
        # Already used: error
        builder.is_valid = pickpack_constants.DB_FALSE
        builder.err_msg = "Serial number %s has already been used." % serial_number
    else:
        # Not already used: ok
        builder.is_valid = pickpack_constants.DB_TRUE
        builder.err_msg = None

    return makeMeADeferred(builder.render())

