def getPackingList_deferred(order_id,
                            ctx,
                           ):
    """
    Returns canned data; used for testing.
    """
    builder = pickpack_result_builders.PackingListBuilder()
    builder.order_number = order_id.number
    builder.order_generation = order_id.generation

    mock_order = MOCK_DATA.get(order_id.concatenated_for_database)

    if mock_order is None:
        builder.packing_list_for_client = pickpack_constants.NO_DATA_FOUND
        builder.item_notes = pickpack_constants.NO_DATA_FOUND
        builder.order_notes = pickpack_constants.NO_DATA_FOUND
        return makeMeADeferred(builder.render())
        #------ End ----------

    is_on_wm_hold = mock_order['order_info'].get('wm_hold', False)
    order_status = mock_order['order_info']['order_status']
    service_level = mock_order['order_info'].get('service_level', 'N')
    carrier_code = mock_order['order_info']['carrier_code']
    try:
        pickpack_common.validate_order(is_on_wm_hold,
                                       order_status,
                                       order_id,
                                       service_level,
                                       carrier_code,
                                      )
    except pickpack_errors.HandleableServerError as err:
        builder.server_error = err.message
        return makeMeADeferred(builder.render())
        #------ End ----------

    packing_list = getPackingList(mock_order)
    if len(packing_list) == 0:
        builder.packing_list_for_client = pickpack_constants.NO_DATA_FOUND
        builder.item_notes = pickpack_constants.NO_DATA_FOUND
        builder.order_notes = pickpack_constants.NO_DATA_FOUND
        return makeMeADeferred(builder.render())
        #------ End ----------

    if len(packing_list.items_not_picked) > 0:
        # Return "items not picked" error to client
        builder.server_error = ("Order %s has one or more items that have "
                                "not been picked. Items: %s."
                                % (order_id.formatted_for_display,
                                   ", ".join(packing_list.items_not_picked),
                                  )
                               )
        return makeMeADeferred(builder.render())
        #------ End ----------

    builder.item_notes = pickpack_common.getItemNotes(packing_list,
                                                      getItemNoteText,
                                                     )

    customer_number = mock_order['order_info']['customer_number']
    #order_entry_initials = mock_order['order_info']['order_entry_initials']
    calcualted_order_weight = mock_order['order_info'].get('calcualted_order_weight', 10)
    ship_to_state = mock_order['order_info'].get('ship_to_state', 'WI')
    ship_to_country = mock_order['order_info'].get('ship_to_country', pickpack_constants.UNITED_STATES_COUNTRY_CODE)
    customer_sold_to_country = mock_order['order_info'].get('customer_sold_to_country', pickpack_constants.UNITED_STATES_COUNTRY_CODE)

    (builder.order_notes,
     inline_images,
    ) = pickpack_common.getOrderNotesAndInlineImages(carrier_code,
                                                     packing_list,
                                                     customer_number,
                                                     #order_entry_initials,
                                                     calcualted_order_weight,
                                                     service_level,
                                                     ship_to_state,
                                                     ship_to_country,
                                                     customer_sold_to_country,
                                                     ctx,
                                                    )

    # Add the inline images to our packing list container object
    packing_list.add_inline_images(inline_images)

    builder.packing_list_for_client = packing_list.client_data()
    return makeMeADeferred(builder.render())

def getPackingList(mock_order):
    """
    Takes a complete mock order from MOCK_DATA. Returns a PackingListContainer
    object containing mock packing list data.
    """
    # Some of the packing list data gets returned to the client as-is. Other
    # data elements are used to calculate aggregate or otherwise digested
    # values. Here we use the mock data to populate a container of objects. This
    # container will hold and transport the packing list data during processing.
    return pickpack_utility_classes.PackingListContainer(mock_order['line_items'])

